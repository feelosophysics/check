#!/usr/bin/env bash

# ==============================================================================
# [setup_env.sh] agent-app-leak 실행을 위한 리눅스 가상 환경 구성 스크립트
# ==============================================================================
# 이 스크립트는 리눅스 초심자들을 위한 가이드라인이 될 수 있도록,
# 사용되는 모든 명령어와 기법들에 대해 "매우 상세한 한글 주석"을 기재하였습니다.
# 이 스크립트를 실행하면 애플리케이션의 홈 디렉토리와 보안 인증 키파일 등이 자동 구축됩니다.
# ==============================================================================

# 1. 실행 계정 검사 (root 계정 실행 방지)
# ------------------------------------------------------------------------------
# 미션 요구사항 4.1에 따라, 애플리케이션은 보안을 위해 'root가 아닌 일반 사용자'로 실행해야 합니다.
# 리눅스에서 현재 사용자의 UID(User ID)를 알아내는 명령어는 'id -u'입니다. root의 UID는 항상 0입니다.
# -eq는 'equal(같다)'을 뜻하는 쉘 스크립트의 비교 연산자입니다.
if [ "$(id -u)" -eq 0 ]; then
    # echo 명령어는 터미널 화면에 텍스트를 출력(인쇄)합니다.
    echo "[오류] 이 프로그램은 보안 상 root 계정으로 실행할 수 없습니다."
    echo "일반 사용자 계정으로 전환한 후 다시 실행해 주세요."
    # exit 1은 스크립트의 실행을 실패 상태(코드 1)로 강제 종료함을 나타냅니다.
    exit 1
fi

# 2. AGENT_HOME 경로 정의
# ------------------------------------------------------------------------------
# 애플리케이션의 설정과 데이터가 담길 메인 홈 디렉토리를 지정합니다.
# 사용자님과 합의한 대로 호스트와 가상 머신이 실시간 공유하는 디렉토리 내의 'agent_home'을 기본값으로 삼습니다.
# 만약 이미 터미널에 AGENT_HOME 환경변수가 설정되어 있다면, 기존 값을 유지하고(${AGENT_HOME:-기본값} 문법),
# 설정되어 있지 않다면 지정한 기본 경로인 '/Users/f22losophysics1091/Desktop/260525work/agent_home'을 대입합니다.
DEFAULT_HOME="/Users/f22losophysics1091/Desktop/260525work/agent_home"
export AGENT_HOME="${AGENT_HOME:-$DEFAULT_HOME}"

echo "======================================================================"
echo "▶ [환경 구성] 가상 머신 내 AGENT_HOME 환경 셋업을 시작합니다."
echo "▶ 지정된 홈 경로: $AGENT_HOME"
echo "======================================================================"

# 3. 필수 디렉토리 목록 정의 및 생성
# ------------------------------------------------------------------------------
# 미션 요구사항 4.1에 맞추어 필수적인 3가지 서브 디렉토리를 생성해야 합니다.
# - AGENT_UPLOAD_DIR: 파일 업로드를 위한 디렉토리 ($AGENT_HOME/upload_files)
# - AGENT_KEY_PATH: 인증 키 관리를 위한 디렉토리 ($AGENT_HOME/api_keys)
# - AGENT_LOG_DIR: 프로세스 및 애플리케이션 로그 기록 디렉토리 ($AGENT_HOME/logs)
UPLOAD_DIR="$AGENT_HOME/upload_files"
KEY_DIR="$AGENT_HOME/api_keys"
LOG_DIR="$AGENT_HOME/logs"

# 'mkdir -p' 명령어는 디렉토리를 생성합니다. 
# -p 옵션은 'parents'의 약자로, 상위 디렉토리가 존재하지 않을 때 상위 디렉토리까지 차례대로 한 번에 자동으로 만들어 줍니다.
# 또한 이미 디렉토리가 존재하더라도 오류를 내지 않고 조용히 넘어가는 유용한 옵션입니다.
echo "1. 필수 디렉토리를 생성하는 중..."
mkdir -p "$UPLOAD_DIR"
mkdir -p "$KEY_DIR"
mkdir -p "$LOG_DIR"

# 4. API Key 파일(secret.key) 구성
# ------------------------------------------------------------------------------
# 미션 요구사항에 명시된 보안 대칭키 파일을 생성합니다.
# 경로: $AGENT_HOME/api_keys/secret.key
# 내용: agent_api_key_test
KEY_FILE="$KEY_DIR/secret.key"

echo "2. API 보안 대칭키 파일(secret.key) 생성 중..."
# echo 명령어 뒤의 문자열을 '>' 기호를 통해 파일로 내보냅니다 (리다이렉션, Redirection).
# '>' 기호는 파일을 매번 새로 만들어 내용을 덮어쓰고, '>>' 기호는 기존 내용 뒤에 덧붙입니다.
# 여기서는 미션에 지정된 정확한 텍스트 값만 담기 위해 '>'를 사용하여 파일을 새로 고쳐 생성합니다.
echo -n "agent_api_key_test" > "$KEY_FILE"

# 5. 권한(Permissions) 설정
# ------------------------------------------------------------------------------
# 리눅스에서는 파일 및 디렉토리의 읽기(r), 쓰기(w), 실행(x) 권한을 제어하여 시스템의 안전을 보장합니다.
# - chmod 700: 소유자(User)에게만 읽기(4), 쓰기(2), 실행(1) 권한을 모두 부여(7=4+2+1)하고, 
#   그룹이나 타인은 아예 접근할 수 없게 만듭니다(00). API 키 및 로그 등의 무단 노출과 수정을 막기 위한 최소한의 조치입니다.
# - chmod 600: 소유자에게만 읽기(4)와 쓰기(2) 권한을 부여(6=4+2)하고, 실행 권한은 제거합니다. 일반 텍스트 파일에 안전합니다.
echo "3. 디렉토리 및 파일 권한 조정 중..."
chmod 700 "$AGENT_HOME"
chmod 700 "$UPLOAD_DIR"
chmod 700 "$KEY_DIR"
chmod 700 "$LOG_DIR"
chmod 600 "$KEY_FILE"

# 6. 최종 셋업 확인 및 검증
# ------------------------------------------------------------------------------
# 작성된 파일과 디렉토리가 요구사항에 맞춰 물리적으로 잘 존재하고 있는지 하나씩 점검합니다.
# -d 검사는 '디렉토리가 존재하는지'를 판단하고, -f 검사는 '일반 파일이 존재하는지'를 판단합니다.
# ! 기호는 부정(NOT)의 의미로, 해당 조건이 참이 아닐 경우(존재하지 않을 경우)를 지칭합니다.
echo "4. 환경 구성 적합성 검증 중..."

# 검증 에러 플래그 설정 (0이면 오류 없음, 1이면 오류 발생)
ERROR_FOUND=0

if [ ! -d "$UPLOAD_DIR" ]; then
    echo "[경고] 업로드 디렉토리가 누락되었습니다: $UPLOAD_DIR"
    ERROR_FOUND=1
fi

if [ ! -d "$KEY_DIR" ]; then
    echo "[경고] API 키 보관 디렉토리가 누락되었습니다: $KEY_DIR"
    ERROR_FOUND=1
fi

if [ ! -d "$LOG_DIR" ]; then
    echo "[경고] 로그 디렉토리가 누락되었습니다: $LOG_DIR"
    ERROR_FOUND=1
fi

if [ ! -f "$KEY_FILE" ]; then
    echo "[경고] secret.key 파일이 존재하지 않습니다: $KEY_FILE"
    ERROR_FOUND=1
else
    # cat 명령어로 키 파일의 내용을 임시 변수에 담고 검증합니다.
    # $() 문법은 괄호 안 명령어의 실행 결과를 문자열로 치환(Command Substitution)해줍니다.
    KEY_CONTENT=$(cat "$KEY_FILE")
    if [ "$KEY_CONTENT" != "agent_api_key_test" ]; then
        echo "[경고] secret.key의 내용이 잘못되었습니다. 현재값: $KEY_CONTENT"
        ERROR_FOUND=1
    fi
fi

# 7. 최종 결과 리포팅
# ------------------------------------------------------------------------------
echo "----------------------------------------------------------------------"
if [ "$ERROR_FOUND" -eq 0 ]; then
    echo "✔ [성공] 가상 머신 환경 구성 및 보안 검증이 완벽히 끝났습니다!"
    echo "✔ 아래 환경변수를 쉘에 등록하신 후 앱을 안전하게 기동해 주세요."
    echo "   export AGENT_HOME=\"$AGENT_HOME\""
    echo "   export AGENT_PORT=15034"
    echo "   export AGENT_UPLOAD_DIR=\"$UPLOAD_DIR\""
    echo "   export AGENT_KEY_PATH=\"$KEY_DIR\""
    echo "   export AGENT_LOG_DIR=\"$LOG_DIR\""
else
    echo "❌ [오류] 환경 구성 중 실패 요소가 감지되었습니다. 상단의 에러 로그를 확인하십시오."
    exit 1
fi
echo "======================================================================"
