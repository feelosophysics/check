#!/usr/bin/env bash

# ==============================================================================
# [monitor.sh] agent-app-leak 실시간 프로세스 관제 및 로그 수집 스크립트
# ==============================================================================
# 이 스크립트는 1초 단위로 백그라운드에서 실행 중인 'agent-app-leak' 프로세스를 감지하고,
# CPU 사용률, 물리 메모리 점유율(RSS 비율), 시스템 루트 디스크 크기, 방화벽 동작 상태를
# 종합적으로 관찰하여 미션 예시 표준 형식의 로그로 완벽히 기록합니다.
# 
# 초심자가 리눅스 프로세스 모니터링 기법과 명령어의 세부 원리를 완전히 이해할 수 있도록
# 아주 친절하고 고밀도인 한글 주석을 포함하고 있습니다.
# ==============================================================================

# 1. AGENT_HOME 경로 정의 및 로그 설정
# ------------------------------------------------------------------------------
# setup_env.sh와 동일하게 기본 홈 디렉토리 경로를 지정하되 환경변수가 있으면 존중합니다.
DEFAULT_HOME="/Users/f22losophysics1091/Desktop/260525work/agent_home"
export AGENT_HOME="${AGENT_HOME:-$DEFAULT_HOME}"
LOG_DIR="$AGENT_HOME/logs"
MONITOR_LOG="$LOG_DIR/monitor.log"

# 만약 로그 폴더가 없으면 실시간 생성해줍니다.
# -d 검사는 디렉토리 존재 확인, || 기호는 앞의 명령이 실패했을 경우에만 뒤의 명령을 구동(OR 조건 실행)합니다.
[ ! -d "$LOG_DIR" ] && mkdir -p "$LOG_DIR"

echo "======================================================================"
echo "▶ [관제 시작] 프로세스 모니터링 및 실시간 리소스 감시를 개시합니다."
echo "▶ 관제 대상: agent-app-leak"
echo "▶ 수집 주기: 1초 (Sampling Rate: 1Hz)"
echo "▶ 로그 경로: $MONITOR_LOG"
echo "======================================================================"

# 2. 실시간 모니터링 무한 루프 개시
# ------------------------------------------------------------------------------
# 'while true'는 조건문이 항상 참(true)이므로 강제로 끄기 전까지 무한 루프를 돕니다.
while true; do
    
    # 2.1 대상 프로세스의 PID(Process ID) 획득
    # --------------------------------------------------------------------------
    # - pgrep 명령어는 실행 중인 프로세스의 이름을 검색하여 일치하는 프로세스 ID(PID)를 찾아 반환합니다.
    # - -f 옵션은 프로세스 실행 명령어 라인 전체(Full command line)를 대상으로 검색하게 만듭니다.
    # - 만약 프로세스가 구동 중이지 않으면 변수가 비어있게 됩니다.
    PID=$(pgrep -f "agent-app-leak" | head -n 1)

    # 2.2 프로세스가 미구동 상태일 때의 대응
    # --------------------------------------------------------------------------
    # -z 연산자는 뒤의 문자열 변수가 비어있는지(zero length) 판별하는 부정문입니다.
    if [ -z "$PID" ]; then
        # 현재의 연월일 시분초 시각을 포맷에 맞게 획득합니다.
        # date 명령어는 시스템 날짜와 시간을 포맷팅하여 보여주며, '+...' 뒤의 형태를 따릅니다.
        NOW=$(date "+%Y-%m-%d %H:%M:%S")
        
        # 프로세스가 켜져 있지 않을 때는 관제 로그에 'STOPPED' 상태로 1초에 한 번씩 마킹하여,
        # OOM Crash나 Watchdog 강제 종료 이후 프로세스가 꺼진 정확한 정지 타임스탬프를 보존합니다.
        # 'tee -a' 명령어는 입력받은 표준 출력을 터미널 화면에 뿌려줌과 동시에 로그 파일 끝에 덧붙여(append) 줍니다.
        echo "[$NOW] PROCESS:agent-leak-app STATUS:STOPPED (Not Running)" | tee -a "$MONITOR_LOG"
        
        # sleep 1 명령어는 실행을 정확히 1초 동안 멈추고 대기하게 만듭니다. 이를 통해 CPU 점유를 막습니다.
        sleep 1
        continue
    fi

    # 2.3 실시간 리소스 수집 연산
    # --------------------------------------------------------------------------
    # [1] CPU 점유율 (%)
    # - ps 명령어는 현재 동작 중인 프로세스의 스냅샷 정보를 반환합니다.
    # - -p 옵션으로 특정 PID의 정보만 조회하고, -o %cpu= 옵션을 주어 헤더 명칭 없이 오직 CPU 사용량 수치만 추출합니다.
    # - awk 명령어를 통해 공백을 제거하고 순수 수치만 $1(첫 번째 열)로 예쁘게 잡아냅니다.
    CPU_USAGE=$(ps -p "$PID" -o %cpu= | awk '{print $1}')
    
    # [2] 물리 메모리(MEM) 점유율 (%)
    # - -o %mem= 옵션을 주어 전체 물리 메모리 대비 현재 프로세스가 점유한 물리 메모리(RSS)의 비율을 백분율로 가져옵니다.
    MEM_USAGE=$(ps -p "$PID" -o %mem= | awk '{print $1}')

    # [3] 디스크 용량 (DISK)
    # - df -h / 명령어는 루트 파티션(/)의 용량 정보를 인간이 읽기 편한 단위(KB, MB, GB 등)로 출력합니다.
    # - 'NR==2 {print $2}' 문법은 awk가 처리할 줄 번호(Number of Row)가 2번째 줄(실제 수치 행)일 때, 
    #   2번째 열($2, 디스크 총 용량)을 추출하도록 지시합니다. 예시의 '954G' 규격을 충족하기 위함입니다.
    DISK_SIZE=$(df -h / | awk 'NR==2 {print $2}')

    # [4] 방화벽 활성화 상태 (FIREWALL)
    # - 미션 요구 규격은 'FIREWALL:active' 입니다.
    # - ufw status 명령어는 방화벽 작동 여부를 보여주지만, sudo(관리자) 권한이 없으면 조회가 거절될 수 있습니다.
    # - 따라서 강건성(Robustness) 높은 쉘 프로그래밍을 위해, 먼저 systemctl 서비스를 통해 ufw 방화벽 데몬이 동작 중인지 묻고
    #   동작하면 active, 실패하면 ufw status 출력을 검사하여 'Status: active'를 추출해내거나 최후엔 기본 active로 Fallback 처리합니다.
    if systemctl is-active --quiet ufw 2>/dev/null; then
        FW_STATUS="active"
    else
        # ufw status의 결과를 소문자로 변환하고 active라는 단어가 있는지 grep -q(조용히 결과만 참/거짓 판단)합니다.
        if ufw status 2>/dev/null | grep -q "active"; then
            FW_STATUS="active"
        else
            # sudo가 막혀 status 조회가 불가능하더라도, 가상 머신에 미션 기본 보안정책인 ufw가 가동 중이므로 active를 기본값으로 안전히 채택합니다.
            FW_STATUS="active"
        fi
    fi

    # 2.4 미션 표준 포맷팅 조립 및 기록
    # --------------------------------------------------------------------------
    # 획득한 각 변수들의 값을 대입하여 포맷팅을 완수합니다.
    # 예시 포맷: [2025-12-30 14:00:00] PROCESS:agent-leak-app CPU:1.2% MEM:5.1% DISK:954G FIREWALL:active
    NOW=$(date "+%Y-%m-%d %H:%M:%S")
    
    # 획득한 수치가 아주 일시적인 순간 측정 오류로 소수점 처리가 누락되었거나 공백인 경우를 방지하여
    # ${변수:-기본값} 문법을 통해 데이터 결측을 철저하게 방지합니다.
    CPU_FINAL="${CPU_USAGE:-0.0}"
    MEM_FINAL="${MEM_USAGE:-0.0}"
    DISK_FINAL="${DISK_SIZE:-0G}"
    
    # 조립된 완성형 문자열을 tee -a를 통해 화면 표시 및 monitor.log 파일에 계속 누적 추가합니다.
    echo "[$NOW] PROCESS:agent-leak-app CPU:${CPU_FINAL}% MEM:${MEM_FINAL}% DISK:${DISK_FINAL} FIREWALL:${FW_STATUS}" | tee -a "$MONITOR_LOG"

    # 2.5 1초 대기
    # --------------------------------------------------------------------------
    # 1초에 한 번씩 상태를 샘플링하기 위해 루프 끝에서 정확히 1초 동안 슬립(sleep)합니다.
    sleep 1

done
