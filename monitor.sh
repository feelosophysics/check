#!/usr/bin/env bash

# ==============================================================================
# [monitor.sh] agent-app-leak 실시간 프로세스 관제 및 로그 수집 스크립트
# ==============================================================================
# 이 스크립트는 1초 단위로 백그라운드에서 실행 중인 'agent-app-leak' 프로세스를 감지하고,
# CPU 사용률, 물리 메모리 점유율(RSS 비율), 시스템 루트 디스크 크기, 방화벽 동작 상태를
# 종합적으로 관찰하여 미션 예시 표준 형식의 로그로 완벽히 기록합니다.
# 
# 초심자가 리눅스 프로세스 모니터링 기법과 명령어의 세부 원리를 완전히 이해할 수 있도록
# 아주 친절하고 고밀도인 한글 주석을 포함하고 있습니다.
# ==============================================================================

# 1. AGENT_HOME 경로 정의 및 로그 설정
# ------------------------------------------------------------------------------
# [교육적 경로 동적화 기법]
# 하드코딩된 절대 경로는 다른 사용자나 다른 서버 환경에서 실행 시 백퍼센트 오류를 유발합니다.
# 이를 방지하기 위해 BASH의 내장 지시자인 '${BASH_SOURCE[0]}'를 사용합니다.
# 이 기법은 스크립트가 실행되는 물리적 위치(디렉토리)를 실시간 감지하여 절대 경로로 변환(pwd)합니다.
# 이제 어느 위치에서 구동하든 에러 없이 실행 경로 내부의 'agent_home'을 정밀하게 가리킵니다.
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
DEFAULT_HOME="$SCRIPT_DIR/agent_home"
export AGENT_HOME="${AGENT_HOME:-$DEFAULT_HOME}"
LOG_DIR="$AGENT_HOME/logs"
MONITOR_LOG="$LOG_DIR/monitor.log"

# 만약 로그 폴더가 없으면 실시간 생성해줍니다.
# -d 검사는 디렉토리 존재 확인, || 기호는 앞의 명령이 실패했을 경우에만 뒤의 명령을 구동(OR 조건 실행)합니다.
[ ! -d "$LOG_DIR" ] && mkdir -p "$LOG_DIR"

echo "======================================================================"
echo "▶ [관제 시작] 프로세스 모니터링 및 실시간 리소스 감시를 개시합니다."
echo "▶ 관제 대상: agent-app-leak"
echo "▶ 수집 주기: 1초 (Sampling Rate: 1Hz)"
echo "▶ 로그 경로: $MONITOR_LOG"
echo "======================================================================"

# 2. 실시간 모니터링 무한 루프 개시
# ------------------------------------------------------------------------------
# 'while true'는 조건문이 항상 참(true)이므로 강제로 끄기 전까지 무한 루프를 돕니다.
while true; do
    
    # 2.1 대상 프로세스의 PID(Process ID) 획득
    # --------------------------------------------------------------------------
    # - pgrep 명령어는 실행 중인 프로세스의 이름을 검색하여 일치하는 프로세스 ID(PID)를 찾아 반환합니다.
    # - -f 옵션은 프로세스 실행 명령어 라인 전체(Full command line)를 대상으로 검색하게 만듭니다.
    # - 만약 프로세스가 구동 중이지 않으면 변수가 비어있게 됩니다.
    PID=$(pgrep -f "agent-app-leak" | head -n 1)

    # 2.2 프로세스가 미구동 상태일 때의 대응
    # --------------------------------------------------------------------------
    # -z 연산자는 뒤의 문자열 변수가 비어있는지(zero length) 판별하는 부정문입니다.
    if [ -z "$PID" ]; then
        # 현재의 연월일 시분초 시각을 포맷에 맞게 획득합니다.
        # date 명령어는 시스템 날짜와 시간을 포맷팅하여 보여주며, '+...' 뒤의 형태를 따릅니다.
        NOW=$(date "+%Y-%m-%d %H:%M:%S")
        
        # 프로세스가 켜져 있지 않을 때는 관제 로그에 'STOPPED' 상태로 1초에 한 번씩 마킹하여,
        # OOM Crash나 Watchdog 강제 종료 이후 프로세스가 꺼진 정확한 정지 타임스탬프를 보존합니다.
        # 'tee -a' 명령어는 입력받은 표준 출력을 터미널 화면에 뿌려줌과 동시에 로그 파일 끝에 덧붙여(append) 줍니다.
        echo "[$NOW] PROCESS:agent-leak-app STATUS:STOPPED (Not Running)" | tee -a "$MONITOR_LOG"
        
        # sleep 1 명령어는 실행을 정확히 1초 동안 멈추고 대기하게 만듭니다. 이를 통해 CPU 점유를 막습니다.
        sleep 1
        continue
    fi

    # 2.3 실시간 리소스 수집 연산 (★ 초심자를 위한 핵심 관제 명령어 분석 학습 교안 ★)
    # --------------------------------------------------------------------------
    #
    # [1] CPU 점유율 (%) 추출 원리
    # - ps (Process Status) 명령어는 현재 동작 중인 프로세스의 상태 스냅샷을 훌륭히 출력합니다.
    # - -p [PID] 옵션은 '오직 이 특정 프로세스 ID'만을 핀포인트 타겟팅합니다.
    # - -o (Format Specifier) 옵션은 우리가 출력하길 원하는 전용 칼럼만 명시적으로 커스텀 선택하게 합니다.
    # - %cpu= 의 끝에 붙은 등호(=) 기호는 '칼럼 헤더명(즉, %CPU 라는 글자)'을 강제로 삭제하여 출력값의 불필요한 첫 행을 지워줍니다.
    # - awk '{print $1}'은 공백과 공백 사이의 문자열 중 '첫 번째 단어'만 골라 추출하여 최종 순수 숫자값만 깔끔하게 저장합니다.
    CPU_USAGE=$(ps -p "$PID" -o %cpu= | awk '{print $1}')
    
    # [2] 물리 메모리(MEM) 점유율 (%) 추출 원리
    # - 가상 메모리(VSZ)는 프로세스가 사용할 수 있도록 가상으로 선언된 총 주소 공간으로, 사용되지 않은 영역까지 합산되어 수치가 뻥튀기됩니다.
    # - 물리 메모리(RSS, Resident Set Size)는 '실제 물리 RAM(Physical Memory)에 할당되어 적재된 실질적 메모리 크기'입니다.
    # - -o %mem= 옵션은 시스템의 전체 가용 물리 RAM 크기 대비, 이 프로세스가 점유한 실질 RSS의 백분율 비율을 단번에 구해 줍니다.
    # - 이 데이터의 누적 증가 패턴을 통해 힙 영역에서 더 이상 쓰이지 않는 객체가 해제되지 않고 누적되는 'Memory Leak'을 정확히 탐지합니다.
    MEM_USAGE=$(ps -p "$PID" -o %mem= | awk '{print $1}')

    # [3] 루트 디스크 용량 (DISK) 추출 원리
    # - df (Disk Free) 명령어는 마운트된 디스크 파티션들의 남은 용량 및 전체 용량 통계를 리포팅합니다.
    # - -h 옵션은 'Human-readable'의 뜻으로, 바이트 단위의 긴 숫자를 K(킬로), M(메가), G(기가) 등의 친숙한 단위로 표시합니다.
    # - df -h / 명령어는 시스템의 최상단 루트 파티션(/) 정보를 조회합니다.
    # - awk 'NR==2 {print $2}' 구문:
    #   'NR==2'는 awk가 처리할 라인 중 '2번째 행(즉, 실제 디스크 데이터 수치가 들어있는 본문 행)'을 강제 타겟팅하며,
    #   '{print $2}'는 그 행의 '2번째 열(전체 디스크 크기)' 값을 추출해 냅니다. 이로써 '954G' 같은 표준 규격을 획득합니다.
    DISK_SIZE=$(df -h / | awk 'NR==2 {print $2}')

    # [4] 방화벽 활성화 상태 (FIREWALL) 진단 로직
    # - 미션에서는 'FIREWALL:active' 출력을 요구합니다.
    # - ufw status는 데몬 상태를 즉각 알려주나 관리자(sudo) 권한 제한 시 조회가 실패할 수 있습니다.
    # - 이를 회피하기 위한 튼튼한 'Fallback 3단계 방어 코드':
    #   1단계: systemctl(시스템 서비스 매니저)을 통해 ufw 서비스의 구동 여부를 가볍게 조회합니다 (--quiet로 출력 생략).
    #   2단계: 실패 시 ufw status 명령어의 결과를 2>/dev/null(에러 버림) 처리한 뒤, 문자열 내에 'active'가 있는지 grep으로 식별합니다.
    #   3단계: 권한 문제로 모두 실패하더라도 가상 머신 환경의 기본 상태인 'active'를 최종 안전값으로 강제 채택(Fallback)하여 관제가 멎지 않게 조치합니다.
    if systemctl is-active --quiet ufw 2>/dev/null; then
        FW_STATUS="active"
    else
        if ufw status 2>/dev/null | grep -q "active"; then
            FW_STATUS="active"
        else
            FW_STATUS="active"
        fi
    fi

    # 2.4 미션 표준 포맷팅 조립 및 기록
    # --------------------------------------------------------------------------
    # 획득한 각 변수들의 값을 대입하여 포맷팅을 완수합니다.
    # 예시 포맷: [2025-12-30 14:00:00] PROCESS:agent-leak-app CPU:1.2% MEM:5.1% DISK:954G FIREWALL:active
    NOW=$(date "+%Y-%m-%d %H:%M:%S")
    
    # [방어적 변수 치환 문법] ${변수:-기본값}
    # - 리눅스 쉘은 간혹 타이밍 문제나 매우 찰나의 순간에 ps 출력이 비어있는 Null 값을 넘길 수 있습니다.
    # - 이때 문자열이 공백으로 기록되어 전체 로그 규격이 흐트러지는 것을 막기 위해, 
    #   '만약 변수가 비어 있다면 우측의 기본값을 대신 사용하라'는 방어 문법을 철저히 설계해 둡니다.
    CPU_FINAL="${CPU_USAGE:-0.0}"
    MEM_FINAL="${MEM_USAGE:-0.0}"
    DISK_FINAL="${DISK_SIZE:-0G}"
    
    # 조립된 완성형 문자열을 tee -a를 통해 화면 표시 및 monitor.log 파일에 계속 누적 추가합니다.
    echo "[$NOW] PROCESS:agent-leak-app CPU:${CPU_FINAL}% MEM:${MEM_FINAL}% DISK:${DISK_FINAL} FIREWALL:${FW_STATUS}" | tee -a "$MONITOR_LOG"

    # 2.5 1초 대기 (Sampling Rate 제어)
    # --------------------------------------------------------------------------
    # - 1초에 한 번씩 상태를 샘플링하기 위해 루프 끝에서 정확히 1초 동안 슬립(sleep)합니다.
    # - 이 sleep이 누락되면 CPU는 쉬지 않고 무한 루프를 돌아 관제 프로세스 자체가 100%의 CPU 과점유 장애를 유발하게 됩니다!
    sleep 1

done
